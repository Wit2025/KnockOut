class Bubble {
  double x;
  double y;
  double radius;
  double speed;
  double opacity;

  Bubble({
    required this.x,
    required this.y,
    required this.radius,
    required this.speed,
    required this.opacity,
  });
}

class OuterBubble {
  double angle;
  double distance;
  double radius;
  double speed;
  double opacity;

  OuterBubble({
    required this.angle,
    required this.distance,
    required this.radius,
    required this.speed,
    required this.opacity,
  });
}